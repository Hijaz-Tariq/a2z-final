// Parse delivery address from "to" string
function parseUSAddress(to: string): { line1: string; city: string; state: string; postalCode: string } {
  const cleaned = to.trim();
  
  // Extract 5-digit ZIP code
  const zipMatch = cleaned.match(/\b(\d{5})\b/);
  const postalCode = zipMatch ? zipMatch[1] : '';

  // Extract 2-letter state (e.g., "CA", "NY", "TX")
  const stateMatch = cleaned.match(/\b([A-Z]{2})\b/);
  const state = stateMatch ? stateMatch[1] : '';

  // Extract city: everything before the state/ZIP
  let city = '';
  if (state || postalCode) {
    // Split by state or ZIP and take the first part
    const parts = cleaned.split(new RegExp(`\\b(${state}|${postalCode})\\b`))[0];
    city = parts.trim().replace(/\s+/g, ' '); // clean extra spaces
  } else {
    // Fallback: assume whole string is city
    city = cleaned;
  }

  return {
    line1: cleaned,
    city,
    state,
    postalCode,
  };
}

async function migrateParcel(parcel: any) {
  // ✅ Skip if no recipient phone (required for Contact)
  if (!parcel.recipientphone || parcel.recipientphone.trim() === '') {
    console.log(`⚠️ Skipping parcel (no recipient phone): ${parcel._id}`);
    return null;
  }

  try {
    // 1. Get or create recipient contact
    let recipient = await prisma.contact.findFirst({
      where: { phone: parcel.recipientphone }
    });
    if (!recipient) {
      recipient = await prisma.contact.create({
        data: {
          name: parcel.recipientname,
          phone: parcel.recipientphone,
          email: parcel.recipientemail || null,
          company: null,
        },
      });
    }

    // 2. Get or create sender contact (if phone exists)
    let senderId: string | null = null;
    if (parcel.senderphone && parcel.senderphone.trim() !== '') {
      let sender = await prisma.contact.findFirst({
        where: { phone: parcel.senderphone }
      });
      if (!sender) {
        sender = await prisma.contact.create({
          data: {
            name: parcel.sendername,
            phone: parcel.senderphone,
            email: parcel.senderemail || null,
            company: null,
          },
        });
      }
      senderId = sender.id;
    }

    // 3. Create pickup address (sender)
    const pickupAddress = await prisma.address.create({
      data: {
        line1: parcel.from || 'N/A',
        line2: null,
        city: '',
        state: '',
        postalCode: '00000',
        country: 'PS', // Palestine
        coordinates: '',
      },
    });

    // 4. Create delivery address (recipient)
    const { line1, city, state, postalCode } = parseUSAddress(parcel.to || '');
    const deliveryAddress = await prisma.address.create({
      data: {
        line1,
        line2: null,
        city,
        state,
        postalCode,
        country: "US",
        coordinates: '',
      },
    });

    // 5. Prepare items
    const items = [];
    if (parcel.oilQuantity > 0) {
      items.push({
        description: 'زيت',
        quantity: parcel.oilQuantity,
        value: 200 * parcel.oilQuantity,
        currency: 'USD',
        weight: parcel.oilQuantity,
      });
    }
    if (parcel.oliveQuantity > 0) {
      items.push({
        description: 'زيتون',
        quantity: parcel.oliveQuantity,
        value: 100 * parcel.oliveQuantity,
        currency: 'USD',
        weight: parcel.oliveQuantity,
      });
    }

    // 6. Create pickup
    const pickup = await prisma.pickup.create({
      data: {
        type: PickupType.OUTBOUND_SHIPMENT,
        scheduledDate: new Date(parcel.date),
        status: ShippingStatus.DELIVERED, // all historical = delivered
        specialNotes: null, // ignore old note
        pickupContactId: senderId || recipient.id, // fallback to recipient if no sender
        deliveryContactId: recipient.id,
        customPickupAddressId: pickupAddress.id,
        customDeliveryAddressId: deliveryAddress.id,
        calculatedCost: parcel.cost || 0,
        costCurrency: 'USD',
        storageFeeAcknowledged: true,
        paymentStatus: 'COMPLETED', // all paid
        items: {
          create: items,
        },
        packages: {
          create: [
            {
              packageType: 'parcel',
              weight: parcel.oilQuantity + parcel.oliveQuantity,
              length: 30,
              width: 20,
              height: 20,
              specialNotes: 'Migrated legacy parcel',
            },
          ],
        },
      },
    });

    console.log(`✅ Migrated parcel: ${pickup.id}`);
    return pickup.id;
  } catch (error) {
    console.error(`❌ Failed to migrate parcel ${parcel._id}:`, error);
    return null;
  }
}

async function main() {
  console.log(`Starting migration of ${legacyParcels.length} parcels...`);
  let successCount = 0;

  for (const parcel of legacyParcels) {
    const result = await migrateParcel(parcel);
    if (result) successCount++;
  }

  console.log(`✅ Migration complete: ${successCount}/${legacyParcels.length} parcels migrated.`);
  await prisma.$disconnect();
}

main().catch((e) => {
  console.error('Migration failed:', e);
  process.exit(1);
});